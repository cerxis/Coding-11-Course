print("area of a trapezoid calculator")
h = input("Enter Height of Trapezoid:")
h = float(h)
length_bottom = input("Enter Length of the bottom base:")
length_bottom = float(length_bottom)
length_top = input("Enter lenght of the top base:")
length_top = float(length_top)
area = 1 / 2 * ( length_bottom + length_top ) * h
print(area)
